CREATE TABLE Person (
    NAME VARCHAR(30),      /* Jane, Doe M           */
    DOA VARCHAR(12),       /* MM/DD/YYYY            */
    PDF VARCHAR(15),       /* <date>.pdf            */
    PIC VARCHAR(15),       /* <date>.jpeg           */
    CRIME VARCHAR(50),     /* Possesion of...       */
    COUNTY VARCHAR(15),    /* Leon, Okuloussa etc.. */
    STATE VARCHAR(2)       /* 2 digit state code    */
);
